create table public.kenya_counties (
  county_order integer primary key,
  name text not null unique,
  slug text not null unique,
  source text not null default 'Constituency schedule extracted from List of Counties, constituencies and wards in Kenya.pdf, PDF pages 127-163'
);
create table public.kenya_constituencies (
  constituency_number integer primary key,
  county_order integer not null references public.kenya_counties(county_order) on delete cascade,
  name text not null,
  slug text not null,
  source_area_text text not null,
  source_pdf_page integer not null,
  unique (county_order, name)
);
create table public.kenya_wards (
  id bigserial primary key,
  constituency_number integer not null references public.kenya_constituencies(constituency_number) on delete cascade,
  county_order integer not null references public.kenya_counties(county_order) on delete cascade,
  ward_order integer not null,
  name text not null,
  slug text not null,
  unique (constituency_number, ward_order)
);
create table public.candidate_position_scopes (
  id text primary key,
  display_name text not null,
  geography_level text not null check (geography_level in ('national','county','constituency','ward')),
  description text not null,
  sort_rank integer not null
);
alter table public.kenya_counties enable row level security;
alter table public.kenya_constituencies enable row level security;
alter table public.kenya_wards enable row level security;
alter table public.candidate_position_scopes enable row level security;
create policy "public can read kenya_counties" on public.kenya_counties for select to anon, authenticated using (true);
create policy "public can read kenya_constituencies" on public.kenya_constituencies for select to anon, authenticated using (true);
create policy "public can read kenya_wards" on public.kenya_wards for select to anon, authenticated using (true);
create policy "public can read candidate_position_scopes" on public.candidate_position_scopes for select to anon, authenticated using (true);
grant select on public.kenya_counties, public.kenya_constituencies, public.kenya_wards, public.candidate_position_scopes to anon, authenticated;
grant usage, select on sequence public.kenya_wards_id_seq to authenticated;
